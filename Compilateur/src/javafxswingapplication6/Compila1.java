private void edit(FileReader K)throws IOException, java.io.IOException {
    BufferedReader lec = new BufferedReader (K);
    String l =null;
    jTextArea1.setText("");
            while(  (l=lec.readLine())!=null)
            {jTextArea1.append(l);
             jTextArea1.append("\n");
            }
            lec.close();
}
private void sauve(File filename) throws IOException 
{       
    FileWriter l = null;
        try {
            l = new FileWriter(filename);
            BufferedWriter S = new BufferedWriter(l);
            S.write(jTextArea1.getText()); System.out.println(jTextArea1.getText());
            S.close();
        } catch (java.io.IOException ex) {
            Logger.getLogger(Compila1.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                l.close();
            } catch (java.io.IOException ex) {
                Logger.getLogger(Compila1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
}
